def comparar_listas_tuplas(lista1, lista2):
    # Verificar la longitud de las listas
    print(len(lista1))
    if len(lista1) != len(lista2):
        print("Las listas tienen diferentes longitudes")
        return

    # Comparar las tuplas en ambas listas
    for i in range(len(lista1)):
        tupla1 = lista1[i]
        tupla2 = lista2[i]

        # Comparar las tuplas elemento por elemento
        if tupla1 != tupla2:
            print(f"Diferencia encontrada en la posición {i}:")
            print(f"Tupla en lista1: {tupla1}")
            print(f"Tupla en lista2: {tupla2}")
            return

    # Si no se encontraron diferencias
    print("Las listas son exactamente iguales")


